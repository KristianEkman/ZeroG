/* Helper: map a 64-square index to a mirrored PST index (0..31) */
static inline int pst_mirror_idx(int sq) {
    int file = sq & 7;
    int rank = sq >> 3;
    int mf = file <= 3 ? file : 7 - file;
    return rank * 4 + mf;
}

